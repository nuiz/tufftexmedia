<script>
var Page = (function(){

    var isActive = false;
    var sectionActive = null;

    var windowH;

    var $back;
    var $sections;
    var $activeEl;
    var $logo;

    function _page(){}
    _page.prototype.refreshRender = function(){
        if(isActive){
            $back.addClass('active');
            $sections.addClass('inactive');
            $activeEl.removeClass('inactive').addClass('active');
            switch($activeEl.attr('id')) {
                case 'about':
                    $logo.css({top: '120%', left: '120%'});
                    break;
                case 'media':
                    $logo.css({top: '120%', left: '-20%'});
                    break;
                case 'investor':
                    $logo.css({top: '-20%', left: '120%'});
                    break;
                case 'our-clients':
                    $logo.css({top: '-20%', left: '-20%'});
                    break;
            }
        }
        else {
            $back.removeClass('active');
            $activeEl.removeClass('active');
            $sections.removeClass('inactive');
            $logo.css({top: '', left: ''});
        }
    };

    _page.prototype.showContact = function(){
        $('#contact').addClass('active');
    };

    _page.prototype.setupParallax1 = function(){
        var $wrap = $("#investor");
        var $parallax = $('.parallax1');
        var h = windowH + $parallax.height();
        $wrap.scroll(function(){
            if($parallax.offset().top < windowH && $parallax.offset().top + $parallax.height() > 0){
                var pos = windowH - $parallax.offset().top;
                var perc = (pos/h)*100;
                console.log("in", perc);
                $parallax.find('.parallax-bg').css('background-position', '0 '+perc+'%');
            }
        });
    };

    _page.prototype.run = function(){
        $back = $('#back');
        $logo = $('#logo');
        $sections = $('.section');
        windowH = $(window).height();

        var that = this;
        $('.spacial-title').click(function(e){
            e.preventDefault();
            if(isActive) return;

            $activeEl = $(this).closest('.section');
            sectionActive = $activeEl.attr('id');
            isActive = true;
            that.refreshRender();
        });

        $back.click(function(e){
            e.preventDefault();
            sectionActive = null;
            isActive = false;

            that.refreshRender();
        });

        $('.contact-toggle-btn').click(function(e){
            e.preventDefault();
            $('#contact').toggleClass('active');
        });

        $('.show-contact-btn').click(function(e){
            e.preventDefault();
            that.showContact();
        });

        that.setupParallax1();
        $(window).resize(function(){
            windowH = $(window).height();
        });
    };

    return _page;
})();

$(function(){
    var page = new Page();
    page.run();
});
